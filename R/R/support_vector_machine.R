#' @return The fitted model.
#' @export
support_vector_machine_fit_model_binomial <- function(X, y, ...) {
  # capture additional arguments
  kwargs <- list(...)
  
  # process kwargs
  kwargs[["x"]] <- as.matrix(X)
  kwargs[["y"]] <- factor(y)
  kwargs[["scale"]] <- FALSE
  kwargs[["type"]] <- "C-classification"
  
